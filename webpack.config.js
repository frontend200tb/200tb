// ============================================================================
// ПОДКЛЮЧЕНИЕ МОДУЛЕЙ
// ============================================================================

// Подключаем модуль 'path' для работы с путями к файлам и папкам
// Нужен, чтобы правильно указывать пути в разных операционных системах (Windows, Mac, Linux)
const path = require('path');

// Плагин для генерации HTML-файла на основе шаблона
// Автоматически подключает собранные скрипты и стили
const HtmlWebpackPlugin = require('html-webpack-plugin');

// Плагин для извлечения CSS в отдельный файл (вместо того чтобы встраивать в JS)
// Улучшает кэширование и параллельную загрузку стилей
const MiniCssExtractPlugin = require('mini-css-extract-plugin');

// ============================================================================
// ЭКСПОРТ КОНФИГУРАЦИИ
// ============================================================================

module.exports = {
  // ===== ТОЧКА ВХОДА =====
  // Главный файл приложения, с которого Webpack начинает сборку
  // Webpack проанализирует все импорты (import/require) внутри этого файла
  entry: './src/index.js',

  // ===== НАСТРОЙКИ ВЫХОДНЫХ ФАЙЛОВ =====
  output: {
    // Очищать папку dist перед каждой сборкой (удаляет старые файлы)
    clean: true,

    // Абсолютный путь к папке, куда будут собраны все файлы
    // __dirname - текущая папка проекта, ./dist - папка назначения
    path: path.resolve(__dirname, './dist'),

    // Имя основного собранного JavaScript файла
    filename: 'index.js',

    // Шаблон имени для ресурсов (изображения, шрифты и т.д.)
    // [name] - оригинальное имя файла, [ext] - его расширение
    // Пример: logo.png -> assets/logo.png
    assetModuleFilename: 'assets/[name][ext]',
  },

  // ===== НАСТРОЙКИ ОБРАБОТКИ ФАЙЛОВ =====
  module: {
    // Массив правил для разных типов файлов
    rules: [
      // ----- ПРАВИЛО ДЛЯ HTML ФАЙЛОВ -----
      {
        // Регулярное выражение: обрабатываем файлы с расширением .html (регистр не важен)
        test: /\.html$/i,

        // Массив загрузчиков (применяются в обратном порядке: снизу вверх)
        use: [
          {
            // Вторым применяется html-loader
            // Обрабатывает HTML, находит ресурсы (img, src и т.д.)
            loader: 'html-loader',
            options: {
              sources: true, // Включает обработку ресурсов (изображения, шрифты)
            },
          },
          {
            // Первым применяется кастомный загрузчик
            // Экранирует угловые скобки < > внутри блоков с C++ кодом
            // Превращает <iostream> в &lt;iostream&gt; для корректного отображения в браузере
            loader: path.resolve(__dirname, 'escape-cpp-loader.js'),
          },
        ],
      },

      // ----- ПРАВИЛО ДЛЯ СТИЛЕЙ (SCSS/SASS/CSS) -----
      {
        // Обрабатываем файлы с расширениями .scss, .sass, .css
        test: /\.(scss|sass|css)$/,

        // Массив загрузчиков (применяются справа налево)
        use: [
          MiniCssExtractPlugin.loader,  // 4. Извлекает CSS в отдельный файл
          'css-loader',                 // 3. Обрабатывает CSS (import, url())
          'postcss-loader',             // 2. Добавляет CSS префиксы (автопрефиксер)
          'sass-loader',                // 1. Компилирует SCSS/SASS в CSS
        ],
      },

      // ----- ПРАВИЛО ДЛЯ JAVASCRIPT -----
      {
        // Обрабатываем все .js файлы
        test: /\.js$/,

        // Исключаем папку node_modules (не нужно обрабатывать чужие библиотеки)
        exclude: /node_modules/,

        // Используем babel-loader для транспиляции современного JS в старый
        use: {
          loader: 'babel-loader',
          // Настройки babel находятся в отдельном файле .babelrc или в package.json
        },
      },

      // ----- ПРАВИЛО ДЛЯ ШРИФТОВ -----
      {
        // Обрабатываем файлы шрифтов с разными расширениями
        test: /\.(otf|ttf|eot|woff|woff2)$/,

        // asset/resource копирует файлы в папку назначения
        type: 'asset/resource',

        // Генератор для выходных файлов
        generator: {
          // Помещаем шрифты в папку fonts, сохраняя оригинальное имя и расширение
          filename: 'fonts/[name][ext]',
        },
      },

      // ----- ПРАВИЛО ДЛЯ ИЗОБРАЖЕНИЙ -----
      {
        // Обрабатываем популярные форматы изображений (регистр не важен)
        test: /\.(png|jpg|jpeg|gif|svg)$/i,

        // asset/resource копирует изображения в папку назначения
        type: 'asset/resource',

        generator: {
          // Помещаем изображения в папку images
          filename: 'images/[name][ext]',
        },
      },
    ],
  },

  // ===== ПЛАГИНЫ =====
  plugins: [
    // Плагин для работы с HTML
    new HtmlWebpackPlugin({
      // Путь к шаблону HTML-файла (исходник)
      template: path.resolve(__dirname, './src/index.html'),

      // Имя выходного файла в папке dist
      filename: 'index.html',

      // Путь к иконке сайта (favicon)
      favicon: './src/favicon.ico',
    }),

    // Плагин для извлечения CSS в отдельный файл
    new MiniCssExtractPlugin({
      // Имя выходного CSS файла
      filename: 'style.css',
    }),
  ],

  // ===== НАСТРОЙКИ DEV SERVER (для разработки) =====
  devServer: {
    // Настройки клиентской части
    client: {
      // Показывать ошибки поверх страницы (оверлей)
      overlay: true,
    },

    // Настройки статических файлов
    static: {
      // Папка, из которой раздаются статические файлы
      directory: path.resolve(__dirname, 'dist'),
    },

    // Следить за файлами в папке src и пересобирать при изменениях
    watchFiles: path.join(__dirname, 'src'),

    // Порт, на котором будет запущен dev-сервер
    port: 9000,

    // Включить Hot Module Replacement (горячую замену модулей)
    // Обновляет код без перезагрузки страницы
    hot: true,
  },

  // ===== Source Maps =====
  // Создаёт карты кода для отладки
  // Позволяет видеть исходный код в инструментах разработчика браузера,
  // несмотря на минификацию и транспиляцию
  devtool: 'source-map',
};